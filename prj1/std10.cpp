#include <iostream>
using namespace std;

int main(void)
{
    int a = 10;
    cout << "aの値は" << a << "です" << endl;
    cout << "a == 10の値は" << (a == 10) << "です" << endl;
    cout << "a == 20の値は" << (a == 20) << "です" << endl;
    cout << "a != 10の値は" << (a != 10) << "です" << endl;
    cout << "a != 20の値は" << (a != 20) << "です" << endl;
    
}